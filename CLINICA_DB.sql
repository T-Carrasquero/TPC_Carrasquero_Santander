Create Database TPC_CLINICA_DB
go
Use TPC_CLINICA_DB
go

Create Table Especialidades(
	Id bigint not null primary key identity (1, 1),
	Descripcion varchar(50) not null
)
go
Create Table Localidades(
	CodigoPostal int not null primary key,
	Nombre varchar(100) not null,
	Provincia varchar(50) not null
)
go
Create Table Medicos(
	Dni varchar(10) not null primary key,
	Apellidos varchar(50) not null,
	Nombres varchar(50) not null,
	Sexo varchar(1) not null,
	CodigoPostal int foreign key references Localidades(CodigoPostal),
	Direccion varchar(50),
	Email varchar(50),
	Telefono varchar(50),
	Matricula varchar(50) not null,
	Estado bit not null default (1)
)
go
create table EspecialidadesPorMedico(
    DniMedico varchar(10) not null foreign key references Medicos(Dni),
    IdEspecialidad bigint not null foreign key references Especialidades(Id),
    primary key (DniMedico, IdEspecialidad)
)
go
create table Pacientes(
	Dni varchar(10) not null primary key,
	Apellidos varchar(50) not null,
	Nombres varchar(50) not null,
	Sexo varchar(1) not null,
	CodigoPostal int foreign key references Localidades(CodigoPostal),
	Direccion varchar(50),
	Email varchar(50),
	Telefono varchar(50),
	Estado bit not null default (1)
)
go
create table Recepcionistas(
	Legajo int not null primary key identity(1,1),
	Apellidos varchar(50) not null,
	Nombres varchar(50) not null,
	Dni varchar(10) not null,
	Sexo varchar(1) not null,
	CodigoPostal int foreign key references Localidades(CodigoPostal),
	Direccion varchar(50),
	Email varchar(50),
	Telefono varchar(50),
	Estado bit not null default (1)
)
go
create table Usuarios_tipos(
	Id bigint not null PRIMARY KEY IDENTITY(1,1),
	Descripcion varchar(50) not null
)
go
create table Usuarios(
	Id bigint not null PRIMARY KEY IDENTITY(1,1),
	Nombre varchar (50) not null UNIQUE,
	Contraseña varchar (50) not null,
	IdTipoUsuario bigint not null foreign key references Usuarios_tipos(Id),
	dniPaciente varchar(10),
	Estado bit not NULL default (1)
)
go
